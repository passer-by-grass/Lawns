//是固定不变的数，是常数。直接写在程序里的叫直接量
//更好的方式是定义一个常量：const int AMOUNT=100 
//const是一个修饰符，加在int的前面，给这个变量加上一个“不变的”的属性，
//表示变量的值一旦初始化了就不能再修改了
//一般const的量名字是大写字母 
